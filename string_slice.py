# ejemplo de sintaxis de slices en python
numeros = '01234567891011121314'
print(numeros)
"""
string[principio:final:pasos]

"""
print(numeros[0])
print('')

print('[0:3]')
print(numeros[0:3])

print('[3:3]')
print(numeros[3:3])

print('[::]')
print(numeros[::])

print('[::-1]')
print(numeros[::-1])

print('[:8:3]')
print(numeros[:8:3])

print('[0:4]')
print(numeros[0:4])

print('[0:6:-1]')
print(numeros[::-1])
